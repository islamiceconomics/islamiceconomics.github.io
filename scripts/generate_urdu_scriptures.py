def read_javascript_file(filepath):
    """Read and parse the JavaScript file"""
    with open(filepath, 'r', encoding='utf-8') as f:
        content = f.read()
    return content

def parse_javascript_object(content):
    """Parse the JavaScript object to extract data"""
    # Extract the entire object definition
    match = re.search(r'const SCRIPTURE_DATA = ({.*?});$', content, re.DOTALL)
    if not match:
        raise ValueError("Could not find SCRIPTURE_DATA object in file")

    js_object = match.group(1)

    # This is a simplified parser - we'll extract topics and entries separately
    topics_match = re.search(r'topics:\s*\[(.*?)\],\s*entries:', js_object, re.DOTALL)
    entries_match = re.search(r'entries:\s*\[(.*?)\]\s*\}', js_object, re.DOTALL)

    if not topics_match or not entries_match:
        raise ValueError("Could not parse topics or entries from file")

    return {
        'topics_str': topics_match.group(1),
        'entries_str': entries_match.group(1)
    }

def extract_topics(topics_str):
    """Extract topic objects from the topics string"""
    topics = []
    # Split by }, { pattern to identify individual topic objects
    topic_pattern = r'{(.*?)}'
    matches = re.finditer(topic_pattern, topics_str, re.DOTALL)

    for match in matches:
        topic_content = match.group(1)
        # Extract individual fields
        id_match = re.search(r'id:\s*"([^"]+)"', topic_content)

        if id_match:
            topic_id = id_match.group(1)
            if topic_id in TOPIC_TRANSLATIONS:
                topics.append({
                    'id': topic_id,
                    'content': topic_content
                })

    return topics

def extract_entries(entries_str):
    """Extract entry objects from the entries string"""
    entries = []
    entry_pattern = r'{(.*?)}'
    matches = re.finditer(entry_pattern, entries_str, re.DOTALL)

    for match in matches:
        entry_content = match.group(1)
        # Extract ID
        id_match = re.search(r'id:\s*(\d+)', entry_content)
        if id_match:
            entry_id = int(id_match.group(1))
            entries.append({
                'id': entry_id,
                'content': entry_content,
                'full': '{' + entry_content + '}'
            })

    return entries

def generate_urdu_translation(entry_id, entry_content):
    """Generate Urdu translation for an entry"""
    # Extract original fields
    id_match = re.search(r'id:\s*(\d+)', entry_content)
    type_match = re.search(r'type:\s*"([^"]+)"', entry_content)
    reference_match = re.search(r'reference:\s*"([^"]+)"', entry_content)
    arabic_match = re.search(r'arabic:\s*"([^"]+)"', entry_content)
    topics_match = re.search(r'topics:\s*\[(.*?)\]', entry_content)
    related_match = re.search(r'relatedIds:\s*\[(.*?)\]', entry_content)
    strength_match = re.search(r'strength:\s*([^,}]+)', entry_content)
    collection_match = re.search(r'collection:\s*([^}]+)', entry_content)

    # Get Urdu translation if available, otherwise use English
    if entry_id in ENTRY_TRANSLATIONS:
        urdu_trans = ENTRY_TRANSLATIONS[entry_id]['translation']
        urdu_context = ENTRY_TRANSLATIONS[entry_id]['context']
    else:
        # Fallback for entries without translations
        trans_match = re.search(r'translation:\s*"([^"]*)"', entry_content)
        context_match = re.search(r'context:\s*"([^"]*)"', entry_content)

        urdu_trans = trans_match.group(1) if trans_match else "ترجمہ دستیاب نہیں"
        urdu_context = context_match.group(1) if context_match else "تفصیل دستیاب نہیں"

    # Build the new entry object
    urdu_entry = f"""    {{
      id: {id_match.group(1)},
      type: "{type_match.group(1)}",
      reference: "{reference_match.group(1)}",
      arabic: "{arabic_match.group(1)}",
      translation: "{urdu_trans}",
      topics: [{topics_match.group(1)}],
      context: "{urdu_context}",
      relatedIds: [{related_match.group(1)}],
      strength: {strength_match.group(1).strip()},
      collection: {collection_match.group(1).strip()}
    }}"""

    return urdu_entry

def escape_urdu_string(text):
    """Escape special characters in Urdu strings for JavaScript"""
    text = text.replace('\\', '\\\\')
    text = text.replace('"', '\\"')
    text = text.replace('\n', '\\n')
    return text

def generate_urdu_file(english_filepath, output_filepath):
    """Main function to generate Urdu scripture data file"""
    print("Reading English scripture data file...")
    content = read_javascript_file(english_filepath)

    print("Parsing JavaScript object...")
    parsed = parse_javascript_object(content)

    print("Extracting topics...")
    topics = extract_topics(parsed['topics_str'])
    print(f"Found {len(topics)} topics")

    print("Extracting entries...")
    entries = extract_entries(parsed['entries_str'])
    print(f"Found {len(entries)} entries")

    print("Generating Urdu file...")

    # Start building the Urdu file
    output = 'const SCRIPTURE_DATA_UR = {\n  topics: [\n'

    # Add topics
    for i, topic in enumerate(topics):
        topic_id = topic['id']
        translation = TOPIC_TRANSLATIONS[topic_id]

        output += f'    {{\n'
        output += f'      id: "{topic_id}",\n'
        output += f'      name: "{escape_urdu_string(translation["name"])}",\n'
        output += f'      nameAr: "العربية", \n'  # Keep Arabic as is
        output += f'      description: "{escape_urdu_string(translation["description"])}",\n'

        # Extract icon and color from original
        icon_match = re.search(r'icon:\s*"([^"]+)"', topic['content'])
        color_match = re.search(r'color:\s*"([^"]+)"', topic['content'])

        icon = icon_match.group(1) if icon_match else ""
        color = color_match.group(1) if color_match else ""

        output += f'      icon: "{icon}",\n'
        output += f'      color: "{color}"\n'
        output += f'    }}'

        if i < len(topics) - 1:
            output += ','
        output += '\n'

    output += '  ],\n  entries: [\n'

    # Add entries
    for i, entry in enumerate(entries):
        urdu_entry = generate_urdu_translation(entry['id'], entry['content'])
        output += urdu_entry

        if i < len(entries) - 1:
            output += ','
        output += '\n'

    output += '  ]\n};\n'

    # Write the output file
    print(f"Writing Urdu file to {output_filepath}...")
    with open(output_filepath, 'w', encoding='utf-8') as f:
        f.write(output)

    print(f"Successfully created {output_filepath}")
    print(f"File contains {len(topics)} topics and {len(entries)} entries")

